/**
 * Checks if text contains profanity
 * @param text - The text to check
 * @returns Object with isProfane boolean and matched words array
 */
export function checkProfanity(text: string): { isProfane: boolean; matches: string[] } {
  if (!text || typeof text !== 'string') {
    return { isProfane: false, matches: [] };
  }

  const lowerText = text.toLowerCase();
  const matches: string[] = [];

  for (const word of badWords) {
    // Create regex to match whole words with word boundaries
    // Also handles common obfuscation like 'f*ck', 'sh!t', etc.
    const pattern = word.split('').join('[*!@#$%^&]*');
    const regex = new RegExp(`\\b${pattern}\\b`, 'i');
    
    if (regex.test(lowerText)) {
      matches.push(word);
    }
  }

  return {
    isProfane: matches.length > 0,
    matches,
  };
}

/**
 * Validates text and throws error if profanity is found
 * @param text - The text to validate
 * @param fieldName - Name of the field being validated (for error message)
 * @throws Error if profanity is detected
 */
export function validateNoProfanity(text: string, fieldName: string = 'Text'): void {
  const result = checkProfanity(text);
  
  if (result.isProfane) {
    throw new Error(`${fieldName} contains inappropriate language. Please update.`);
  }
}

/**
 * Sanitizes text by censoring profanity
 * @param text - The text to sanitize
 * @returns Sanitized text with profanity replaced by asterisks
 */
export function censorProfanity(text: string): string {
  if (!text || typeof text !== 'string') {
    return text;
  }

  let sanitized = text;

  for (const word of badWords) {
    const pattern = word.split('').join('[*!@#$%^&]*');
    const regex = new RegExp(`\\b${pattern}\\b`, 'gi');
    const replacement = '*'.repeat(word.length);
    sanitized = sanitized.replace(regex, replacement);
  }

  return sanitized;
}
